package jtetris.common;

/**
 * Represents the game state.
 */
public enum GameState {
    /**
     * Game has not started yet.
     */
    NotStarted,

    /**
     * Game is started.
     */
    Started,

    /**
     * Game is paused.
     */
    Paused,

    /**
     * Game is over.
     */
    GameOver
}
